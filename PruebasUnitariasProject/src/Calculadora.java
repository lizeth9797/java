
public class Calculadora {
	public int suma(int a, int b) {
		return a+b;
	}
	public int multiplicacion(int a, int b) {
		return a*b;
	}
	public int divide(int a, int b) {
		return a/b;
	}
	public float getPI() {
		return 3.141592f;
	}
	public String getStrPI() {
		return String.valueOf(getPI());
	}
}
